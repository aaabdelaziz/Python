def weekday_name(day_number):
    match day_number:
        case 0:
            return "Sunday"
        case 1:
            return "Monday"
        case 2:
            return "Tuesday"
        case 3:
            return "Wednesday"
        case 4:
            return "Thursday"
        case 5:
            return "Friday"
        case 6:
            return "Saturday"
        case _:
            return "Invalid day number"
        
print(weekday_name(1))  # Output: Monday
print(weekday_name(7))  # Output: Invalid day number

